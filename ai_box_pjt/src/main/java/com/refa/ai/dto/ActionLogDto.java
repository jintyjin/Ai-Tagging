package com.refa.ai.dto;

public class ActionLogDto {
	private int action_idx;
	private String action_ip;
	private String action_channel;
	private String action_event;
	private String action_action;
	private String action_target;
	private String action_src;
	
	public int getAction_idx() {
		return action_idx;
	}
	public void setAction_idx(int action_idx) {
		this.action_idx = action_idx;
	}
	public String getAction_ip() {
		return action_ip;
	}
	public void setAction_ip(String action_ip) {
		this.action_ip = action_ip;
	}
	public String getAction_channel() {
		return action_channel;
	}
	public void setAction_channel(String action_channel) {
		this.action_channel = action_channel;
	}
	public String getAction_event() {
		return action_event;
	}
	public void setAction_event(String action_event) {
		this.action_event = action_event;
	}
	public String getAction_action() {
		return action_action;
	}
	public void setAction_action(String action_action) {
		this.action_action = action_action;
	}
	public String getAction_target() {
		return action_target;
	}
	public void setAction_target(String action_target) {
		this.action_target = action_target;
	}
	public String getAction_src() {
		return action_src;
	}
	public void setAction_src(String action_src) {
		this.action_src = action_src;
	}
}
