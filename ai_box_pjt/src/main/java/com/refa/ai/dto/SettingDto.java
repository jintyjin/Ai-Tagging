package com.refa.ai.dto;

public class SettingDto {
	private int setting_idx;
	private String setting_name;
	private String setting_drivec;
	private String setting_drived;
	
	public int getSetting_idx() {
		return setting_idx;
	}
	public void setSetting_idx(int setting_idx) {
		this.setting_idx = setting_idx;
	}
	public String getSetting_name() {
		return setting_name;
	}
	public void setSetting_name(String setting_name) {
		this.setting_name = setting_name;
	}
	public String getSetting_drivec() {
		return setting_drivec;
	}
	public void setSetting_drivec(String setting_drivec) {
		this.setting_drivec = setting_drivec;
	}
	public String getSetting_drived() {
		return setting_drived;
	}
	public void setSetting_drived(String setting_drived) {
		this.setting_drived = setting_drived;
	}
}
