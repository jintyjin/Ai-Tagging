package com.refa.ai.dto;

public class PoliceDto {
	private String img_data;
	private String img_type;
	private String event_request;
	private String img_name;
	private String user_name;
	private String user_passwd;
	public String getImg_data() {
		return img_data;
	}
	public void setImg_data(String img_data) {
		this.img_data = img_data;
	}
	public String getImg_type() {
		return img_type;
	}
	public void setImg_type(String img_type) {
		this.img_type = img_type;
	}
	public String getEvent_request() {
		return event_request;
	}
	public void setEvent_request(String event_request) {
		this.event_request = event_request;
	}
	public String getImg_name() {
		return img_name;
	}
	public void setImg_name(String img_name) {
		this.img_name = img_name;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_passwd() {
		return user_passwd;
	}
	public void setUser_passwd(String user_passwd) {
		this.user_passwd = user_passwd;
	}
	
}
